function objectUrlToFile(objectUrl, fileName = "cropped-photo.jpg") {
  return fetch(objectUrl)
    .then((res) => res.blob())
    .then((blob) => new File([blob], fileName, { type: "image/jpeg" }));
}

function loadImageFromFile(file) {
  return new Promise((resolve, reject) => {
    const img = new Image();
    img.onload = () => {
      URL.revokeObjectURL(img.src);
      resolve(img);
    };
    img.onerror = reject;
    img.src = URL.createObjectURL(file);
  });
}

async function fileToExactDataUrl(
  file,
  { outputSize = 650, jpegQuality = 0.9, maxBytes = 650000 } = {}
) {
  const img = await loadImageFromFile(file);

  const canvas = document.createElement("canvas");
  canvas.width = outputSize;
  canvas.height = outputSize;

  const ctx = canvas.getContext("2d");
  if (!ctx) throw new Error("Canvas context not available.");

  ctx.fillStyle = "#ffffff";
  ctx.fillRect(0, 0, outputSize, outputSize);

  ctx.imageSmoothingEnabled = true;
  ctx.imageSmoothingQuality = "high";

  ctx.drawImage(img, 0, 0, outputSize, outputSize);

  let quality = Math.max(0.72, Math.min(0.95, jpegQuality));
  let dataUrl = canvas.toDataURL("image/jpeg", quality);

  while (dataUrl.length > maxBytes * 1.37 && quality > 0.74) {
    quality = Math.max(0.74, quality - 0.03);
    dataUrl = canvas.toDataURL("image/jpeg", quality);
  }

  return dataUrl;
}

