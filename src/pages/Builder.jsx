function EmailSuggestInput({ value, onChange, className, placeholder }) {
  const [focused, setFocused] = useState(false);
  const inputRef    = useRef(null);
  const wrapperRef  = useRef(null);
  const innerRef    = useRef(null);
  const blurTimer   = useRef(null);
  const isAccepting = useRef(false);

  const suggestions = getEmailSuggestions(value, focused);
  const showTray    = suggestions.length > 0;

  /* -- Drive wrapper height via scrollHeight for smooth layout -- */
  useEffect(() => {
    const wrapper = wrapperRef.current;
    const inner   = innerRef.current;
    if (!wrapper) return;

    if (showTray && focused) {
      const targetH = inner ? inner.scrollHeight + 10 : 46;
      wrapper.style.height        = targetH + "px";
      wrapper.style.opacity       = "1";
      wrapper.style.transform     = "translateY(0)";
      wrapper.style.pointerEvents = "auto";
    } else {
      wrapper.style.height        = "0px";
      wrapper.style.opacity       = "0";
      wrapper.style.transform     = "translateY(-6px)";
      wrapper.style.pointerEvents = "none";
    }
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [showTray, focused, suggestions.map((s) => s.full).join(",")]);

  /* -- Reliable cross-browser cursor placement -- */
  const placeCursor = (pos) => {
    // Double rAF: first waits for React render commit, second for layout
    requestAnimationFrame(() => {
      requestAnimationFrame(() => {
        const el = inputRef.current;
        if (!el) return;
        el.focus();
        try { el.setSelectionRange(pos, pos); } catch (_) {}
      });
    });
  };

  /* -- Apply suggestion + place cursor correctly -- */
  const handlePillAccept = (s) => {
    clearTimeout(blurTimer.current);
    isAccepting.current = true;

    const atIdx = value.lastIndexOf("@");

    let newValue, cursorPos;

    if (!value.includes("@") || value.trim() === "") {
      // No username typed → "@domain.com", cursor at 0 so user types username
      newValue  = "@" + s.label;
      cursorPos = 0;

    } else if (atIdx !== -1 && !value.slice(atIdx + 1)) {
      // Has "john@" but nothing after → complete domain, cursor before @
      const local = value.slice(0, atIdx + 1);
      newValue  = local + s.label;
      cursorPos = atIdx;

    } else {
      // Partial domain typed → complete it, cursor before @
      const resultAtIdx = s.full.lastIndexOf("@");
      newValue  = s.full;
      cursorPos = resultAtIdx >= 0 ? resultAtIdx : s.full.length;
    }

    onChange({ target: { value: newValue } });
    setFocused(true);
    placeCursor(cursorPos);

    // Allow normal blur events after cursor placed
    setTimeout(() => { isAccepting.current = false; }, 200);
  };

  /* -- Keyboard: Tab / Enter applies first suggestion silently -- */
  const handleKeyDown = (e) => {
    if ((e.key === "Tab" || e.key === "Enter") && suggestions.length > 0) {
      e.preventDefault();
      handlePillAccept(suggestions[0]);
    }
  };

  const handleFocus = () => {
    clearTimeout(blurTimer.current);
    setFocused(true);
  };

  const handleBlur = () => {
    if (isAccepting.current) return;
    blurTimer.current = setTimeout(() => setFocused(false), 150);
  };

  return (
    <div>
      <input
        ref={inputRef}
        className={className}
        type="text"
        inputMode="email"
        value={value}
        onChange={onChange}
        onKeyDown={handleKeyDown}
        onFocus={handleFocus}
        onBlur={handleBlur}
        placeholder={placeholder}
        autoComplete="off"
        autoCapitalize="none"
        autoCorrect="off"
        spellCheck={false}
      />

      {/*
       * Collapsible tray — height animated via JS using scrollHeight.
       * No margin-top toggling. Opacity + translateY for visual polish.
       * pointerEvents:none while hidden prevents ghost interactions.
       */}
      <div
        ref={wrapperRef}
        aria-live="polite"
        style={{
          overflow:      "hidden",
          height:        "0px",
          opacity:       0,
          transform:     "translateY(-6px)",
          pointerEvents: "none",
          transition:    "height 0.28s ease, opacity 0.2s ease, transform 0.22s ease",
        }}
      >
        <div
          ref={innerRef}
          style={{
            display:       "flex",
            flexWrap:      "wrap",
            alignItems:    "center",
            gap:           "6px",
            paddingTop:    "8px",
            paddingBottom: "2px",
          }}
        >
          {suggestions.map((s) => (
            <EmailPill
              key={s.full}
              label={s.isDefault && value.includes("@") ? s.full : s.label}
              onAccept={() => handlePillAccept(s)}
            />
          ))}
        </div>
      </div>
    </div>
  );
}

