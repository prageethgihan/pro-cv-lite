/* -----------------------------------------------------------------------
   EmailSuggestInput — production-quality collapsible suggestion tray
   ─────────────────────────────────────────────────────────────────────
   Cursor rules after pill accept:
     • Empty input → value "@domain.com", cursor at position 0
       User immediately types username from the start.
     • "john@" + default pill  → "john@domain.com", cursor before @
     • Partial domain typed   → completed, cursor before @

   Animation:
     scrollHeight-driven height + opacity 0→1 + translateY(-6px→0)
     No margin-top toggling. Smooth in-flow layout expansion.

   Click stability:
     pointerdown fires BEFORE blur → pill accept always wins the race.
     150ms blur delay as safety net for touch devices.
     Cancel blur timeout on re-focus.
------------------------------------------------------------------------ */
const EMAIL_DOMAINS  = ["gmail.com", "outlook.com", "hotmail.com", "yahoo.com", "icloud.com"];
const EMAIL_DEFAULTS = ["gmail.com", "outlook.com", "icloud.com"];
const MAX_SUGGESTIONS = 3;

function getEmailSuggestions(value, focused) {
  if (!focused) return [];

  const atIdx = value.lastIndexOf("@");

  // Empty field → show 3 default domains
  if (value.trim() === "") {
    return EMAIL_DEFAULTS.slice(0, MAX_SUGGESTIONS).map((d) => ({
      label: d,
      full: "@" + d,
      isDefault: true,
    }));
  }

  if (atIdx !== -1) {
    const local       = value.slice(0, atIdx + 1);
    const domainTyped = value.slice(atIdx + 1).toLowerCase();

    if (!domainTyped) {
      // Typed up to "@", nothing after → 3 defaults
      return EMAIL_DEFAULTS.slice(0, MAX_SUGGESTIONS).map((d) => ({
        label: d,
        full:  local + d,
        isDefault: true,
      }));
    }

    // Filter by startsWith, show up to MAX_SUGGESTIONS
    const matches = EMAIL_DOMAINS
      .filter((d) => d.startsWith(domainTyped) && d !== domainTyped)
      .slice(0, MAX_SUGGESTIONS);

    if (matches.length === 0) return [];
    return matches.map((d) => ({
      label: d,
      full:  local + d,
      isDefault: false,
    }));
  }

  return [];
}

